/**
 * Para que esta funcion sume diferentes valores deberemos pasarle dos diferentes valores
 */
function sumar(a, b) { //a y b son parametros de funci√≥n
    console.log(a+b);
}
function resta(a, b) {
    console.log(a-b);
}
function multiplicacion(a, b) {
    console.log(a*b);
}
function division(a, b) {
    console.log(a/b);
}

sumar(2, 3);    //estos se conocen como argumentos
resta(5, 3);    //estos se conocen como argumentos
multiplicacion(2, 5);   //estos se conocen como argumentos
division(25, 5);    //estos se conocen como argumentos


